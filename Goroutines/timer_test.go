package Goroutines

import (
	"fmt"
	"sync"
	"testing"
	"time"
)

// representasi kejadian
// ketika timer habis maka even akan dikirimkan ke dalam channel
func TestTimer(t *testing.T) {
	timer := time.NewTimer(5 * time.Second)
	fmt.Println(time.Now())

	time := <-timer.C
	fmt.Println(time)
}

// jika hanya butuh channelnya tanpa butuh data timernya
func TestAfter(t *testing.T) {
	channel := time.After(5 * time.Second)
	fmt.Println(time.Now())

	time := <-channel
	fmt.Println(time)
}

// menjalankan function dengan delay waktu tertentu
// tidak perlu channel hanya perlu functionnya
func TestAfterFunc(t *testing.T) {
	group := sync.WaitGroup{}
	group.Add(1)

	time.AfterFunc(5*time.Second, func() {
		fmt.Println(time.Now())
		group.Done()
	})
	fmt.Println(time.Now())

	group.Wait()
}

// ticker
// representasi kejadian yang terjadi berulang
// setiap waktu tertentu akan mengirimkan data ke channel
func TestTicker(t *testing.T) {
	ticker := time.NewTicker(1 * time.Second)

	go func() {
		time.Sleep(5 * time.Second)
		ticker.Stop()
	}()

	for time := range ticker.C {
		fmt.Println(time)
	}
}

// butuh channelnya saja
func TestTick(t *testing.T) {
	channel := time.Tick(1 * time.Second)

	for time := range channel {
		fmt.Println(time)
	}
}
